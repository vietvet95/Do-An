    private void showAllProductComboBox() throws SQLException {
        productList = ProductController.listAllProduct();
        jComboBoxProduct.removeAllItems();
        List<String> productNameInRecipe = new ArrayList<>();
        List<String> AllproductName = new ArrayList<>();
        for (Product p1 : productList) {
            AllproductName.add(p1.getProductName());
        }
        for (Product p2 : productListInRecipe) {
            productNameInRecipe.add(p2.getProductName());
        }
        for (String name : AllproductName) {
            if (!productNameInRecipe.contains(name)) {
                jComboBoxProduct.addItem(name);
            }
        }
    }

