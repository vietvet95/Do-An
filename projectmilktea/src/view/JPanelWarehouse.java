    private void jButtonMaterialAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMaterialAddActionPerformed
        // TODO add your handling code here:
        String materialID = jTextFieldRawMaterialID.getText();
        String materialname = jTextFieldRawMaterialName.getText();
        String dateAdded = AppUtil.format(jDateChooserRawMaterial.getDate());
        String unit = jTextFieldUnit.getText();
        if (materialID.equals("") || materialname.equals("") || unit.equals("")) {
            JOptionPane.showMessageDialog(null, "Xin vui nhập đầy đủ thông tin!");
        } else {
            try {
                int amount = Integer.parseInt(jTextFieldRawMaterialAmount.getText());
                int price = Integer.parseInt(jTextFieldRawMaterialPrice.getText());
                Material material = new Material(materialID, materialname, dateAdded, price, amount, unit);
                MaterialController.insertMaterial(material);
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                showListDateAddedMaterial();
                showListMaterialAll();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Bạn cần phải nhập số vào ô số lượng và giá nguyên liệu, xin vui lòng nhập lại");
            } catch (SQLException ex) {
                Logger.getLogger(JPanelWarehouse.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Thêm không thành công, xin vui lòng thử lại!");
            }
        }
    }//GEN-LAST:event_jButtonMaterialAddActionPerformed

