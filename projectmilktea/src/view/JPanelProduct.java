    private void showDuLieuProduct() {
        try {
            jTableProduct.removeAll();
            String[] arr = {"Mã sản phẩm", "Tên sản phẩm", "Số Lượng", "Giá"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            String query = "select p.productName,p.productID,p.amountRemaining,p.price from products p INNER JOIN productType t ON p.productTypeID=t.productTypeID";
            Connection connection = ConnectionDB.getConnection();
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("productID"));
                vector.add(rs.getString("productName"));
                vector.add(rs.getInt("amountRemaining"));
                vector.add(rs.getInt("price"));
                model.addRow(vector);
            }
            jTableProduct.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void findProduct(JTable table, String valueToSearch) {
        try {
            Connection con = ConnectionDB.getConnection();
            PreparedStatement ps;
            ps = con.prepareStatement("select p.productID,p.productName,p.amountRemaining,p.price"
                    + " from products p INNER JOIN productType t ON p.productTypeID=t.productTypeID where p.productName LIKE ?");
            ps.setString(1, "%" + valueToSearch + "%");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) table.getModel();

            Object[] row;

            while (rs.next()) {
                row = new Object[4];
                row[0] = rs.getString(1);
                row[1] = rs.getString(2);
                row[2] = rs.getInt(3);
                row[3] = rs.getString(4);

                model.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(JPanelProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void showAllTypeProductComboBox() throws SQLException {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        String query = " select distinct productTypeName from productType";
        Connection connection = ConnectionDB.getConnection();
        PreparedStatement ps = connection.prepareStatement(query);
        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            String type = rs.getString("productTypeName");
            jComboBox1.addItem(type);
            jComboBox2.addItem(type);
        }
        rs.close();
    }


