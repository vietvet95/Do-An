    private void showDataProduct() {
        try {
            jTableProduct.removeAll();
            String[] arr = {"Mã sản phẩm", "Tên sản phẩm", "Số Lượng", "Giá"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            String query = "select p.productName,p.productID,p.amountRemaining,p.price from products p INNER JOIN productType t ON p.productTypeID=t.productTypeID";
            Connection connection = ConnectionDB.getConnection();
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("productID"));
                vector.add(rs.getString("productName"));
                vector.add(rs.getInt("amountRemaining"));
                vector.add(rs.getInt("price"));
                model.addRow(vector);
            }
            jTableProduct.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void showDataProductTypeClick(String valueSelected) {
        try {
            jTableProduct.removeAll();
            String[] arr = {"Mã sản phẩm", "Tên Sản Phẩm", "Số Lượng", "Giá"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            String query = "select p.productID ,p.productName,p.amountRemaining,p.price \n"
                    + "from products p JOIN productType pt ON p.productTypeID=pt.productTypeID where pt.productTypeName = ?";
            Connection connection = ConnectionDB.getConnection();
            PreparedStatement ps = connection.prepareStatement(query);
            ps.setString(1, valueSelected);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("productID"));
                vector.add(rs.getString("productName"));
                vector.add(rs.getInt("amountRemaining"));
                vector.add(rs.getInt("price"));
                model.addRow(vector);
            }
            jTableProduct.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelOrder.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private boolean checkTrungTypeID() {
        try {
            Connection connection = ConnectionDB.getConnection();
            String query = "SELECT * FROM dbo.[productType] WHERE productTypeID = ?";
            PreparedStatement ps = connection.prepareStatement(query);
            ps.setString(1, txtProductTypeID.getText());
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                return true;
            }
            ConnectionDB.closeConnection(connection);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }


