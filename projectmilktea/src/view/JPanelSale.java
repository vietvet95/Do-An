    private void showDialogPromotionEdit(int selectPromotion) throws ParseException, SQLException {

        jDialogPromotionEdit.setSize(600, 600);
        jDialogPromotionEdit.setAlwaysOnTop(true);
        jDialogPromotionEdit.setVisible(true);
        TableModel model = jTablePromotion.getModel();
        promotionID = model.getValueAt(selectPromotion, 0).toString();
        String discount = model.getValueAt(selectPromotion, 1).toString();
        String dateStart = model.getValueAt(selectPromotion, 2).toString();
        String dateEnd = model.getValueAt(selectPromotion, 3).toString();
        jPromotionDiscountEdit.setText(discount);
        jDateChooserPromotinStartEdit.setDate(AppUtil.formatString(dateStart));
        jDateChooserPromotionEndEdit.setDate(AppUtil.formatString(dateEnd));
        String FOPID = PromotionController.getFOPByPromotionId(promotionID);
        pushCOmboBoxFOP(jComboBoxPromotinEdit);
        for (FOP fop : listFOP) {
            if (fop.getFOPID().equals(FOPID)) {
                FOPName = fop.getFOPName();
                jTextFieldFOPEditPromotion.setText(FOPName);
            }
        }
        jTextFieldFOPEditPromotion.setEditable(false);
    }


