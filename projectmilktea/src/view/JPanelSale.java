    private void showDuLieuComboDetails(String valueSelected) {
        try {
            jTableComboDetails.removeAll();
            String[] arr = {"Mã Sản phẩm", "Tên sản phẩm", "Số Lượng", "Giá"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);
            List<Product> productByCombo = ComboController.listProductByComboID(valueSelected);
            for (Product p : productByCombo) {
                Vector vector = new Vector();
                vector.add(p.getProductID());
                vector.add(p.getProductName());
                vector.add(p.getAmountInCombo());
                vector.add(p.getProductPrice() * p.getAmountInCombo());
                model.addRow(vector);
            }
            jTableComboDetails.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(JPanelOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
