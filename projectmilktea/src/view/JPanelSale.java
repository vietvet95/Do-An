    private void showDataPtomotionByFOPId(String FOPNameByListFOP) {
        jTablePromotion.removeAll();
        String[] arr = {"Mã khuyến mãi", "Giảm giá", "Ngày bắt đầu", "Ngày kết thúc"};
        DefaultTableModel model = new DefaultTableModel(arr, 0);
        List<Promotion> promotionList = new ArrayList<>();
        try {
            promotionList = PromotionController.searchPromotion(getFOPIDByName(FOPNameByListFOP));
        } catch (SQLException ex) {
            Logger.getLogger(JPanelSale.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Promotion p : promotionList) {
            Vector vector = new Vector();
            vector.add(p.getPromotionID());
            vector.add(p.getPromotionDiscount());
            vector.add(p.getDateStart());
            vector.add(p.getDateEnd());
            model.addRow(vector);
        }
        jTablePromotion.setModel(model);
    }

    private String getFOPIDByName(String name) {
        String id = null;
        for (FOP fop : listFOP) {
            if (fop.getFOPName().equals(name)) {
                id = fop.getFOPID();
            }
        }
        return id;
    }

