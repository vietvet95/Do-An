    private void addProduct(JTable jTable) {
        int i = jTableProductAdd.getSelectedRow();
        TableModel model = jTableProductAdd.getModel();
        String j = (String) jComboBoxSLProduct.getSelectedItem();
        int slProduct = Integer.parseInt(j);
        String k = model.getValueAt(i, 3).toString();
        int priceProduct = Integer.parseInt(k);
        int sumPrice = priceProduct * slProduct;
        DefaultTableModel modelOrder = (DefaultTableModel) jTable.getModel();
        modelOrder.addRow(new Object[]{model.getValueAt(i, 0).toString(), model.getValueAt(i, 1), slProduct, sumPrice});
        jDialogAddProduct.setVisible(false);
    }

