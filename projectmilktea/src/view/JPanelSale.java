    private void insertPromotionData() throws SQLException {
        try {
            String promotionType = jTextFieldTypePromotion.getText();
            String promotionID = jTextFieldPromotionID.getText();
            String promotionTypeID = jTextFieldFOPID.getText();
            String dateStart = AppUtil.format(jDateChooserStart.getDate());
            String dateEnd = AppUtil.format(jDateChooserEnd.getDate());
            float discount = Float.parseFloat(jTextFieldPromotionDiscount.getText());
            if (PromotionController.checkPromotionID(promotionID)) {
                JOptionPane.showMessageDialog(null, "Mã khuyễn mãi đã tồn tại");
            } else if (promotionType.equals("") || promotionTypeID.equals("") || promotionID.equals("")) {
                JOptionPane.showMessageDialog(null, "Bạn phải nhập đầy đủ thông tin!");
            } else if (checkDatePromotion()) {
                JOptionPane.showMessageDialog(null, "Ngày kết thúc phải sau ngày bắt đầu");
            } else {
                int i = jComboBoxFOP.getSelectedIndex();
                if (i > 0) {
                    PromotionController.insertPromotion(new Promotion(promotionID, String.valueOf(discount), dateStart, dateEnd), new FOP(promotionTypeID, promotionType));
                    JOptionPane.showMessageDialog(null, "Thêm khuyến mãi thành công!");
                } else {
                    if (FOPController.checkFOPID(jTextFieldFOPID.getText())) {
                        JOptionPane.showMessageDialog(null, "Mã loại hình khuyễn mãi đã tồn tại");
                    } else {
                        FOPController.insertFOP(new FOP(promotionTypeID, promotionType));
                        PromotionController.insertPromotion(new Promotion(promotionID, String.valueOf(discount), dateStart, dateEnd), new FOP(promotionTypeID, promotionType));
                        JOptionPane.showMessageDialog(null, "Thêm khuyến mãi thành công!");
                    }
                }
            }
        } catch (NullPointerException exception) {
            JOptionPane.showMessageDialog(null, "Bạn nhập nhập giảm giá từ 0 - 1(0% - 100%)");
        }
    }

